//Operadores: Asignacion, Comparacion y Aritmetricos

//Aritmetricos, o son conocidos como operadores binarios
3 + 2 //Suma, nos regresa el valor de 5
50 - 10 //Resta, nos regresa el valor de 40
10 * 20 //Multiplicacion, nos regresa el valor de 200
20 / 2 //Division, nos regresa 10
21 % 5 //Operador de residuo, nos devuelve 1
'Leiker' + 'Gomez' //Concatena dos valores string

!false //nos regresa el valor contrario, 'true'
!true //nos regresa el valor contrario, 'false'

//Operadores de asignacion
var a = 1;

// operadores de comparacion
3 == '3' //A pesar que es un string, sale true
3 === '3' // sale false, porque tiene que ser exactamente igual.
5 < 3 // sale falso
5 > 3 // sale true
5 <= 6 //sale true
5 >= 6 //sale false

a && b // si a es true, y b es true, se cumple la condicion
a || b // alguna de las dos tiene que ser verdad para cumplir

var edad = 40
edad++ // incrementa edad por 1
edad += 2 // incrementa 2
edad += 5 // incrementa 5